    public void actualizarTablaCliente() {
        Optional<ArrayList<Cliente>> clientesObtenidos = ClienteController.getAll();

        if (clientesObtenidos.isPresent()) {
            this.clientes = clientesObtenidos.get();

            DefaultTableModel dtm = new DefaultTableModel(0, 0);
            String header[] = new String[]{"ID", "Nombre completo", "DNI", "RUC", "Correo", "Celular", "Dirección"};
            dtm.setColumnIdentifiers(header);
            this.tblClientes.setModel(dtm);

            for (Cliente c : clientes) {
                dtm.addRow(new Object[]{
                    c.getId(),
                    c.getNombreCompleto(),
                    c.getDni(),
                    c.getRuc(),
                    c.getCorreo(),
                    c.getCelular(),
                    c.getDireccion()
                });
            }
        }
    }
