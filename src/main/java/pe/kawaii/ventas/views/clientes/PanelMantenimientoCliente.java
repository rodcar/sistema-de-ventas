    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
        String nombreCompleto = this.txtNombreCompleto.getText();
        String dni = this.txtDni.getText();
        String ruc = this.txtRuc.getText();
        String correo = this.txtCorreo.getText();
        String celular = this.txtCelular.getText();
        String direccion = this.txtDireccion.getText();

        Cliente cliente = new Cliente();
        cliente.setNombreCompleto(nombreCompleto);
        cliente.setDni(dni);
        cliente.setRuc(ruc);
        cliente.setCorreo(correo);
        cliente.setCelular(celular);
        cliente.setDireccion(direccion);

        ClienteController.registrar(cliente);
        actualizarTablaCliente();
        JOptionPane.showMessageDialog(null, "El usuario se registró correctamente", "Registro", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_btnCrearActionPerformed

