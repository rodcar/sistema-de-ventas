    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if (clienteSeleccionado != null) {
            int id = this.clienteSeleccionado.getId();
            ClienteController.eliminar(id);
            actualizarTablaCliente();
            this.clienteSeleccionado = null;
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        int rowIndex = this.tblClientes.getSelectedRow();
        if (rowIndex != -1) {
            this.clienteSeleccionado = this.clientes.get(rowIndex);
            VentanaActualizarCliente ventanaActualizar = new VentanaActualizarCliente(this, this.clienteSeleccionado);
            ventanaActualizar.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
            ventanaActualizar.setVisible(true);
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void tblClientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblClientesMouseClicked
        int rowIndex = this.tblClientes.getSelectedRow();
        this.clienteSeleccionado = this.clientes.get(rowIndex);
    }//GEN-LAST:event_tblClientesMouseClicked


